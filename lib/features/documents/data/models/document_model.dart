import 'dart:convert';
import 'package:docguard/features/documents/domain/entities/document_entity.dart';

class DocumentModel extends DocumentEntity {
  const DocumentModel({
    required super.id,
    required super.userId,
    super.categoryId,
    super.ownerFullName,
    super.docNumber,
    super.description,
    super.city,
    super.neighborhood,
    super.countryCode,
    super.fileUrl,
    super.fileExtension,
    super.fileUrlWord,
    super.fileUrlPdfPro,
    super.fileUrlMarkdown,
    super.extractedContent,
    super.reportType,
    super.status,
    super.aiProcessed = false,
    super.isFavorite = false,
    super.thumbnailUrl,
    super.thumbnailExtension,
    super.fileSize,
    super.aiMetadata,
    super.createdAt,
    super.updatedAt,
  });

  @override
  DocumentModel copyWith({
    String? id,
    String? userId,
    String? categoryId,
    String? ownerFullName,
    String? docNumber,
    String? description,
    String? city,
    String? neighborhood,
    String? countryCode,
    String? fileUrl,
    String? fileExtension,
    String? fileUrlWord,
    String? fileUrlPdfPro,
    String? fileUrlMarkdown,
    String? extractedContent,
    String? reportType,
    String? status,
    bool? aiProcessed,
    bool? isFavorite,
    String? thumbnailUrl,
    String? thumbnailExtension,
    int? fileSize,
    Map<String, dynamic>? aiMetadata,
    DateTime? createdAt,
    DateTime? updatedAt,
  }) {
    return DocumentModel(
      id: id ?? this.id,
      userId: userId ?? this.userId,
      categoryId: categoryId ?? this.categoryId,
      ownerFullName: ownerFullName ?? this.ownerFullName,
      docNumber: docNumber ?? this.docNumber,
      description: description ?? this.description,
      city: city ?? this.city,
      neighborhood: neighborhood ?? this.neighborhood,
      countryCode: countryCode ?? this.countryCode,
      fileUrl: fileUrl ?? this.fileUrl,
      fileExtension: fileExtension ?? this.fileExtension,
      fileUrlWord: fileUrlWord ?? this.fileUrlWord,
      fileUrlPdfPro: fileUrlPdfPro ?? this.fileUrlPdfPro,
      fileUrlMarkdown: fileUrlMarkdown ?? this.fileUrlMarkdown,
      extractedContent: extractedContent ?? this.extractedContent,
      reportType: reportType ?? this.reportType,
      status: status ?? this.status,
      aiProcessed: aiProcessed ?? this.aiProcessed,
      isFavorite: isFavorite ?? this.isFavorite,
      thumbnailUrl: thumbnailUrl ?? this.thumbnailUrl,
      thumbnailExtension: thumbnailExtension ?? this.thumbnailExtension,
      fileSize: fileSize ?? this.fileSize,
      aiMetadata: aiMetadata ?? this.aiMetadata,
      createdAt: createdAt ?? this.createdAt,
      updatedAt: updatedAt ?? this.updatedAt,
    );
  }

  factory DocumentModel.fromRow(Map<String, dynamic> row) {
    return DocumentModel(
      id: row['id'] as String,
      userId: row['user_id'] as String,
      categoryId: row['category_id'] as String?,
      ownerFullName: row['owner_full_name'] as String?,
      docNumber: row['doc_number'] as String?,
      description: row['description'] as String?,
      city: row['city'] as String?,
      neighborhood: row['neighborhood'] as String?,
      countryCode: row['country_code'] as String?,
      fileUrl: row['file_url'] as String?,
      fileExtension: row['file_extension'] as String?,
      fileUrlWord: row['file_url_word'] as String?,
      fileUrlPdfPro: row['file_url_pdf_pro'] as String?,
      fileUrlMarkdown: row['file_url_markdown'] as String?,
      extractedContent: row['extracted_content'] as String?,
      reportType: row['report_type'] as String?,
      status: row['status'] as String?,
      aiProcessed: (row['ai_processed'] as int?) == 1,
      isFavorite: (row['is_favorite'] as int?) == 1,
      thumbnailUrl: row['thumbnail_url'] as String?,
      thumbnailExtension: row['thumbnail_extension'] as String?,
      fileSize: row['file_size'] as int?,
      aiMetadata: row['ai_metadata'] != null
          ? jsonDecode(row['ai_metadata'] as String) as Map<String, dynamic>
          : null,
      createdAt: row['created_at'] != null
          ? DateTime.parse(row['created_at'] as String)
          : null,
      updatedAt: row['updated_at'] != null
          ? DateTime.parse(row['updated_at'] as String)
          : null,
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'user_id': userId,
      'category_id': categoryId,
      'owner_full_name': ownerFullName,
      'doc_number': docNumber,
      'description': description,
      'city': city,
      'neighborhood': neighborhood,
      'country_code': countryCode,
      'file_url': fileUrl,
      'file_extension': fileExtension,
      'file_url_word': fileUrlWord,
      'file_url_pdf_pro': fileUrlPdfPro,
      'file_url_markdown': fileUrlMarkdown,
      'extracted_content': extractedContent,
      'report_type': reportType,
      'status': status,
      'ai_processed': aiProcessed ? 1 : 0,
      'is_favorite': isFavorite ? 1 : 0,
      'thumbnail_url': thumbnailUrl,
      'thumbnail_extension': thumbnailExtension,
      'file_size': fileSize,
      'ai_metadata': aiMetadata != null ? jsonEncode(aiMetadata) : null,
      'created_at': createdAt?.toIso8601String(),
      'updated_at': updatedAt?.toIso8601String(),
    };
  }

  DocumentEntity toEntity() {
    return DocumentEntity(
      id: id,
      userId: userId,
      categoryId: categoryId,
      ownerFullName: ownerFullName,
      docNumber: docNumber,
      description: description,
      city: city,
      neighborhood: neighborhood,
      countryCode: countryCode,
      fileUrl: fileUrl,
      fileExtension: fileExtension,
      fileUrlWord: fileUrlWord,
      fileUrlPdfPro: fileUrlPdfPro,
      fileUrlMarkdown: fileUrlMarkdown,
      extractedContent: extractedContent,
      reportType: reportType,
      status: status,
      aiProcessed: aiProcessed,
      isFavorite: isFavorite,
      thumbnailUrl: thumbnailUrl,
      thumbnailExtension: thumbnailExtension,
      fileSize: fileSize,
      aiMetadata: aiMetadata,
      createdAt: createdAt,
      updatedAt: updatedAt,
    );
  }
}
